const axios = require('axios');
const fs = require('fs');
const path = require('path');

const CR_URL = 'https://rules.fabtcg.com/en/cr/08-keywords/';
const MAX_TEXT_LENGTH = 1500;

const outputFile = path.join(
  __dirname,
  '..',
  'src',
  'data',
  'keywords',
  'generated',
  'crText.ts'
);
const glossaryFile = path.join(
  __dirname,
  '..',
  'src',
  'data',
  'keywords',
  'glossary.ts'
);

const HEADING_ALIASES = {
  'go-fish': 'go-fish',
  'evo-upgrade': 'evo-upgrade'
};

const kebab = (raw) =>
  raw
    .toLowerCase()
    .replace(/[’']/g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');

const decodeEntities = (text) =>
  text
    .replace(/&nbsp;/g, ' ')
    .replace(/&amp;/g, '&')
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/&quot;/g, '"')
    .replace(/&#0?39;/g, "'")
    .replace(/&rsquo;|&#8217;/g, '’')
    .replace(/&ldquo;|&#8220;/g, '“')
    .replace(/&rdquo;|&#8221;/g, '”')
    .replace(/&mdash;|&#8212;/g, '—')
    .replace(/&ndash;|&#8211;/g, '–')
    .replace(/&#(\d+);/g, (_, code) => String.fromCharCode(Number(code)));

const stripTags = (html) =>
  decodeEntities(
    html
      .replace(/<(script|style)[\s\S]*?<\/\1>/gi, '')
      .replace(/<br\s*\/?>/gi, '\n')
      .replace(/<\/(p|div|li|h[1-6]|tr)>/gi, '\n')
      .replace(/<[^>]+>/g, '')
  )
    .replace(/[ \t]+/g, ' ')
    .replace(/\s*\n\s*/g, '\n')
    .trim();

const readGlossaryIds = () => {
  const source = fs.readFileSync(glossaryFile, 'utf8');
  const ids = new Set();
  const re = /id: '([a-z0-9-]+)'/g;
  let m;
  while ((m = re.exec(source)) !== null) ids.add(m[1]);
  return ids;
};

axios
  .get(CR_URL)
  .then((response) => {
    const html = response.data;
    const anchorRe = /id="(cr(\d+)\.(\d+)\.(\d+))"/g;
    const anchors = [];
    let m;
    while ((m = anchorRe.exec(html)) !== null) {
      anchors.push({ anchor: m[1], index: m.index });
    }
    console.log(`Top-level rule anchors found: ${anchors.length}`);
    if (anchors.length === 0) {
      throw new Error(
        'No crX.Y.Z anchors found - the CR page structure changed; update the scraper.'
      );
    }

    const glossaryIds = readGlossaryIds();
    const crText = {};
    const matchedHeadings = new Set();
    const unmatchedHeadings = [];

    for (let i = 0; i < anchors.length; i++) {
      const sectionHtml = html.slice(
        anchors[i].index,
        i + 1 < anchors.length ? anchors[i + 1].index : anchors[i].index + 20000
      );
      const text = stripTags(sectionHtml);
      const numbered = text.match(/(\d+\.\d+\.\d+\.?)\s*([\s\S]*)$/);
      if (!numbered) continue;
      const body = numbered[2];
      const firstLine = stripTags(
        sectionHtml.match(/<strong>([\s\S]*?)<\/strong>/i)?.[1] ?? ''
      );
      const nameMatch = firstLine.match(
        /^([A-Za-z0-9()\/’' -]+?)(?:\s+(?:is|are|and|means)\b|[.,:(\n]|$)/
      );
      if (!nameMatch) continue;
      const heading = nameMatch[1].trim().replace(/\s+X$/, '');
      const id = HEADING_ALIASES[kebab(heading)] || kebab(heading);

      if (!glossaryIds.has(id)) {
        unmatchedHeadings.push(`${anchors[i].anchor}: ${heading}`);
        continue;
      }
      if (crText[id]) continue;

      matchedHeadings.add(id);
      let ruleText = body.replace(/\n+/g, '\n').trim();
      if (ruleText.length > MAX_TEXT_LENGTH) {
        ruleText = `${ruleText.slice(0, MAX_TEXT_LENGTH).trimEnd()}…`;
      }
      crText[id] = { anchor: anchors[i].anchor, text: ruleText };
    }

    const missingGlossary = [...glossaryIds].filter(
      (id) => !matchedHeadings.has(id)
    );
    console.log(`Matched glossary entries: ${matchedHeadings.size}`);
    console.log(
      `Glossary ids without CR text (expected for 'term' entries): ${missingGlossary.join(', ')}`
    );
    console.log(
      `CR sections without glossary entry (candidates to add): \n${unmatchedHeadings.join('\n')}`
    );

    const exportData =
      '// Generated by scripts/cr-text-generator.js. Do not edit manually.\n' +
      '// Run `npm run generate-keywords` to regenerate.\n' +
      "import { CrRule } from '../types';\n\n" +
      `export const CR_TEXT: Record<string, CrRule> = ${JSON.stringify(crText, null, 2)};\n`;

    fs.mkdirSync(path.dirname(outputFile), { recursive: true });
    fs.writeFileSync(outputFile, exportData);
    console.log(`CR text written to ${outputFile}`);
  })
  .catch((err) => {
    console.error('Error fetching or processing CR text:');
    console.error(err.message);
    process.exitCode = 1;
  });
