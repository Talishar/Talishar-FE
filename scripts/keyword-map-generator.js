const axios = require('axios');
const fs = require('fs');
const path = require('path');

const url =
  'https://raw.githubusercontent.com/the-fab-cube/flesh-and-blood-cards/main/json/english/card.json';

const outputFile = path.join(
  __dirname,
  '..',
  'src',
  'data',
  'keywords',
  'generated',
  'cardKeywordMap.ts'
);

const NOISE_KEYWORDS = new Set(['Attack', 'Action', 'Hero', 'Weapon']);
const EFFECT_ALLOWLIST = new Set([
  'Freeze',
  'Intimidate',
  'Opt',
  'Meld',
  'Transform',
  'Transcend',
  'Amp',
  'Charge',
  'Reload',
  'Mark'
]);

function getCardIdentifier(name, pitch) {
  if (name === 'Goldfin Harpoon') return 'goldfin_harpoon_yellow';
  let cardID = name.toLowerCase();
  cardID = cardID.split('//').join('_');
  const explicit = { ā: 'a', ä: 'a', ö: 'o', ü: 'u', ß: 's', ṣ: 's' };
  cardID = cardID.replace(/[āäöüßṣ]/g, (ch) => explicit[ch]);
  cardID = cardID.normalize('NFD').replace(/[̀-ͯ]/g, '');
  cardID = cardID.replace(/ /g, '_');
  cardID = cardID.replace(/-/g, '_');
  cardID = cardID.replace(/[^a-z0-9 _]/g, '');
  cardID = cardID.replace(/__/g, '_');
  const suffix =
    { 1: '_red', 2: '_yellow', 3: '_blue' }[String(pitch)] !== undefined
      ? { 1: '_red', 2: '_yellow', 3: '_blue' }[String(pitch)]
      : '';
  return cardID + suffix;
}

function collectKeywords(card) {
  const keywords = [];
  const seen = new Set();
  const push = (kw) => {
    const trimmed = (kw || '').trim();
    if (!trimmed) return;
    const base = trimmed.replace(/\s+\d+$/, '');
    if (NOISE_KEYWORDS.has(base)) return;
    if (seen.has(trimmed.toLowerCase())) return;
    seen.add(trimmed.toLowerCase());
    keywords.push(trimmed);
  };

  for (const kw of card.card_keywords || []) push(kw);
  for (const kw of card.granted_keywords || []) push(kw);
  for (const kw of card.ability_and_effect_keywords || []) {
    const base = (kw || '').trim().replace(/\s+\d+$/, '');
    if (EFFECT_ALLOWLIST.has(base)) push(kw);
  }
  return keywords;
}

axios
  .get(url)
  .then((response) => {
    const cards = response.data;
    console.log(`Total cards fetched: ${cards.length}`);

    const stringTable = [];
    const stringIndex = new Map();
    const map = {};
    const distinctKeywords = new Set();

    const internKeyword = (kw) => {
      if (!stringIndex.has(kw)) {
        stringIndex.set(kw, stringTable.length);
        stringTable.push(kw);
      }
      return stringIndex.get(kw);
    };

    let mappedCards = 0;
    for (const card of cards) {
      const keywords = collectKeywords(card);
      keywords.forEach((kw) => distinctKeywords.add(kw));
      if (keywords.length === 0) continue;
      mappedCards++;

      const indexes = keywords.map(internKeyword);
      const keys = new Set();

      for (const printing of card.printings || []) {
        if (printing.id) keys.add(printing.id);
      }
      const name = (card.name || '').trim();
      if (name) {
        keys.add(getCardIdentifier(name, card.pitch));
        if (name.includes('//')) {
          for (const half of name.split('//')) {
            keys.add(getCardIdentifier(half.trim(), card.pitch));
          }
        }
      }

      for (const key of keys) {
        if (!key) continue;
        if (map[key] !== undefined) {
          // Two different cards can share a name-based id only via reprints of
          // the same card, which carry identical keywords; last write wins.
        }
        map[key] = indexes;
      }
    }

    console.log(`Cards with keywords: ${mappedCards}`);
    console.log(`Distinct keyword strings (${distinctKeywords.size}):`);
    console.log([...distinctKeywords].sort().join('\n'));

    const exportData =
      '// Generated by scripts/keyword-map-generator.js. Do not edit manually.\n' +
      '// Run `npm run generate-keywords` to regenerate.\n\n' +
      `export const KEYWORD_STRINGS: string[] = ${JSON.stringify(stringTable)};\n\n` +
      `export const CARD_KEYWORD_MAP: Record<string, number[]> = ${JSON.stringify(map)};\n`;

    fs.mkdirSync(path.dirname(outputFile), { recursive: true });
    fs.writeFileSync(outputFile, exportData);
    console.log(`Card keyword map written to ${outputFile}`);
  })
  .catch((err) => {
    console.error('Error fetching or processing cards:');
    console.error(err.message);
    if (err.response) {
      console.error(`Status: ${err.response.status}`);
    }
    process.exitCode = 1;
  });
